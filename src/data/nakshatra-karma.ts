// 108 நட்சத்திர பாத கர்ம விவரங்கள் (Nakshatra Padam Karma details)
// Based on classical Vedic / Nadi tradition: each of the 27 nakshatras has 4 padams,
// mapped to a Navamsa rasi. The karma reflects tendencies / past-life impressions.

export interface PadamKarma {
  padam: number;                // 1..4
  navamsaRasi: string;          // Tamil navamsa rasi
  karma: string;                // past-life karma / core tendency
  palan: string;                // present-life effect
  parikaram: string;            // suggested remedy
}

export interface NakshatraKarma {
  index: number;                // 1..27
  name: string;                 // Tamil name
  lord: string;                 // planetary lord (Tamil)
  deity: string;                // presiding deity (Tamil)
  symbol: string;               // symbol (Tamil)
  padams: PadamKarma[];
}

const NAV = [
  "மேஷம்","ரிஷபம்","மிதுனம்","கடகம்","சிம்மம்","கன்னி",
  "துலாம்","விருச்சிகம்","தனுசு","மகரம்","கும்பம்","மீனம்",
];

// helper to compute navamsa rasi for nakshatra n (0..26) padam p (1..4)
// each padam = 3°20'. Navamsa starts at Aries for movable signs of Ashwini/Magha/Moola etc.
function navamsaFor(nakIndex: number, padam: number): string {
  // 108 navamsas cycle through 12 rasis: navamsa index = (nakIndex*4 + padam-1) % 12
  return NAV[(nakIndex * 4 + (padam - 1)) % 12];
}

