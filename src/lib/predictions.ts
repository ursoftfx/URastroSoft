// Guru (Jupiter) bala for today — based on weekday + Jupiter's natal position
export function guruBalamToday(jupiterRasi: number, lagnaRasi: number): { score: number; verdict: string; advice: string } {
  const house = ((jupiterRasi - lagnaRasi + 12) % 12) + 1;
  const today = new Date().getDay();
  const isThursday = today === 4;
  let score = 50;
  // Strong houses for Jupiter
  if ([1, 4, 5, 7, 9, 10, 11].includes(house)) score += 25;
  if ([6, 8, 12].includes(house)) score -= 15;
  // Jupiter exalted in Cancer (3) or own (Sagittarius=8, Pisces=11)
  if ([3, 8, 11].includes(jupiterRasi)) score += 15;
  // Capricorn debilitation (9)
  if (jupiterRasi === 9) score -= 20;
  if (isThursday) score += 10;
  score = Math.max(0, Math.min(100, score));
  const verdict = score >= 75 ? "சிறந்த குரு பலம்" : score >= 55 ? "நல்ல குரு பலம்" : score >= 35 ? "சாதாரண குரு பலம்" : "குறைவான குரு பலம்";
  const advice = isThursday
    ? "இன்று வியாழக்கிழமை — குரு பகவான் வழிபாடு, மஞ்சள் ஆடை, பாயசம் நிவேதனம் சிறப்பு."
    : "வியாழக்கிழமை குரு வழிபாடு, மஞ்சள் தானம், கடலை பருப்பு தானம் செய்யவும்.";
  return { score, verdict, advice };
}
