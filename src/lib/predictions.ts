export interface BhavaPalan {
  bhavaIdx: number;       // 1-12
  bhavaName: string;
  rasi: string;
  lord: string;
  lordHouse: number;      // bhava where lord sits
  palan: string;
}

export function bhavaPalans(lagnaIdx: number, planetRasis: Record<string, number>, rasis: string[]): BhavaPalan[] {
  const out: BhavaPalan[] = [];
  for (let i = 0; i < 12; i++) {
    const rasiIdx = (lagnaIdx + i) % 12;
    const lord = RASI_LORDS_TAMIL[rasiIdx];
    const planetKeyMap: Record<string, string> = {
      "செவ்வாய்": "mars", "சுக்ரன்": "venus", "புதன்": "mercury",
      "சந்திரன்": "moon", "சூரியன்": "sun", "குரு": "jupiter", "சனி": "saturn",
    };
    const lordKey = planetKeyMap[lord];
    const lordRasi = planetRasis[lordKey];
    const lordHouse = ((lordRasi - lagnaIdx + 12) % 12) + 1;
    const palan = `${BHAVA_NAMES[i]} அதிபதி ${lord} ${lordHouse}-ம் வீட்டில் — ${BHAVA_PHRASES[lordHouse - 1]}`;
    out.push({
      bhavaIdx: i + 1,
      bhavaName: BHAVA_NAMES[i],
      rasi: rasis[rasiIdx],
      lord,
      lordHouse,
      palan,
    });
  }
  return out;
}

