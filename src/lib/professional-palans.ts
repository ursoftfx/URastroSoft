// Sani-related yogas: kandagasani, ashtamasani, ezhara sani
export interface SaniYogas {
  ezharaiSani: { active: boolean; phase: string; details: string };
  ashtamaSani: { active: boolean; details: string };
  kandakaSani: { active: boolean; details: string };
}

// Active Saturn-from-Moon position determines current Sani state
export function computeSaniYogas(natalSaturnRasi: number, moonRasi: number): SaniYogas {
  const houseFromMoon = ((natalSaturnRasi - moonRasi + 12) % 12) + 1;
  const ezhar = [12, 1, 2].includes(houseFromMoon);
  const phase = houseFromMoon === 12 ? "முதல் கட்டம் (மங்கல சனி)"
    : houseFromMoon === 1 ? "உச்சக் கட்டம் (பொற் சனி)"
    : houseFromMoon === 2 ? "மூன்றாம் கட்டம் (மறை சனி)"
    : "—";
  return {
    ezharaiSani: {
      active: ezhar,
      phase,
      details: ezhar
        ? `சனி சந்திர ராசியிலிருந்து ${houseFromMoon}-ம் வீட்டில் — ஏழரை சனி ${phase} நடைபெறுகிறது.`
        : `சனி சந்திர ராசியிலிருந்து ${houseFromMoon}-ம் வீட்டில் — ஏழரை சனி இல்லை.`,
    },
    ashtamaSani: {
      active: houseFromMoon === 8,
      details: houseFromMoon === 8
        ? "சனி சந்திரனிலிருந்து 8-ம் வீட்டில் — அஷ்டம சனி நடைபெறுகிறது. ஆரோக்கியம், செலவு கவனிக்கவும்."
        : "அஷ்டம சனி இல்லை.",
    },
    kandakaSani: {
      active: houseFromMoon === 4,
      details: houseFromMoon === 4
        ? "சனி சந்திரனிலிருந்து 4-ம் வீட்டில் — கண்டக சனி நடைபெறுகிறது. வீடு, தாய், மன அமைதியில் சோதனை."
        : "கண்டக சனி இல்லை.",
    },
  };
}

